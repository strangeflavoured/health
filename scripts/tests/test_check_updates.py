class TestCompileUpgraded:
    def test_failure_appends_to_errors(self, tmp_path):
        in_file = tmp_path / "requirements.in"
        in_file.write_text("requests\n")
        out_file = tmp_path / "upgraded.txt"
        errors: list[dict] = []

        with patch("subprocess.run") as mock_run:
            mock_run.return_value = MagicMock(returncode=1, stderr="compile error\n")
            result = check_updates.compile_upgraded(in_file, out_file, errors)

        assert result is False
        assert len(errors) == 1
        assert errors[0]["file"] == str(in_file)

    def test_success_returns_true(self, tmp_path):
        in_file = tmp_path / "requirements.in"
        in_file.write_text("requests\n")
        out_file = tmp_path / "upgraded.txt"
        errors: list[dict] = []

        def fake_run(_cmd, **_kwargs):
            out_file.write_text("requests==2.31.0\n")
            return MagicMock(returncode=0, stderr="", stdout="")

        with patch("subprocess.run", side_effect=fake_run):
            result = check_updates.compile_upgraded(in_file, out_file, errors)

        assert result is True
        assert errors == []

    def test_failure_stderr_is_truncated_to_last_20_lines(self, tmp_path):
        """Error capture keeps only the tail of stderr to bound report size."""
        in_file = tmp_path / "requirements.in"
        in_file.write_text("requests\n")
        out_file = tmp_path / "upgraded.txt"
        errors: list[dict] = []

        # 50 lines of stderr; only the last 20 must be retained.
        long_stderr = "\n".join(f"error line {i}" for i in range(50))
        with patch("subprocess.run") as mock_run:
            mock_run.return_value = MagicMock(returncode=1, stderr=long_stderr)
            check_updates.compile_upgraded(in_file, out_file, errors)

        assert len(errors) == 1
        stderr_tail = errors[0]["stderr"]
        assert len(stderr_tail) == 20
        # The tail must end with line 49, the very last line.
        assert "error line 49" in stderr_tail[-1]

    def test_command_does_not_use_shell(self, tmp_path):
        """``subprocess.run`` must receive a list, never shell=True."""
        in_file = tmp_path / "requirements.in"
        in_file.write_text("requests\n")
        out_file = tmp_path / "upgraded.txt"
        errors: list[dict] = []

        with patch("subprocess.run") as mock_run:
            mock_run.return_value = MagicMock(returncode=0, stderr="", stdout="")
            check_updates.compile_upgraded(in_file, out_file, errors)

        kwargs = mock_run.call_args.kwargs
        # shell=True would be a critical injection path; verify it's never set.
        assert kwargs.get("shell", False) is False
        # And the first positional arg is a list (argv form).
        assert isinstance(mock_run.call_args.args[0], list)


