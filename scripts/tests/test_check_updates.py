class TestParsePins:
    def test_simple_pin(self):
        pins = check_updates.parse_pins("requests==2.28.0\n")
        assert pins["requests"] == "2.28.0"

    def test_multiple_pins(self):
        text = "requests==2.28.0\nflask==3.0.0\ngunicorn==21.2.0\n"
        pins = check_updates.parse_pins(text)
        assert pins["flask"] == "3.0.0"
        assert pins["gunicorn"] == "21.2.0"

    def test_strips_inline_comment(self):
        pins = check_updates.parse_pins("requests==2.28.0  # via -r requirements.in\n")
        assert pins["requests"] == "2.28.0"

    def test_strips_env_marker(self):
        pins = check_updates.parse_pins('requests==2.28.0; python_version >= "3.8"\n')
        assert pins["requests"] == "2.28.0"

    def test_case_insensitive_key(self):
        pins = check_updates.parse_pins("Requests==2.28.0\n")
        assert "requests" in pins

    def test_ignores_hash_lines(self):
        text = textwrap.dedent("""\
            requests==2.28.0 \\
                --hash=sha256:abcdef1234567890
        """)
        pins = check_updates.parse_pins(text)
        assert "hash" not in pins
        assert "--hash" not in pins

    def test_ignores_comment_lines(self):
        pins = check_updates.parse_pins(
            "# This file is autogenerated\nrequests==2.28.0\n"
        )
        assert len(pins) == 1

    def test_ignores_empty_lines(self):
        pins = check_updates.parse_pins("\n\n\nrequests==2.28.0\n\n")
        assert len(pins) == 1

    def test_ignores_non_pin_entries(self):
        pins = check_updates.parse_pins(
            "-r base.txt\n--index-url https://pypi.org\nrequests==2.28.0\n"
        )
        assert "requests" in pins
        assert len(pins) == 1

    def test_empty_string_returns_empty_dict(self):
        assert check_updates.parse_pins("") == {}

    def test_normalises_package_name_with_dash(self):
        pins = check_updates.parse_pins("my-package==1.0\n")
        assert "my-package" in pins

    def test_version_with_dots(self):
        pins = check_updates.parse_pins("numpy==1.24.3\n")
        assert pins["numpy"] == "1.24.3"

    def test_version_with_plus_local_segment(self):
        """PEP 440 local versions like 1.0+local should parse."""
        pins = check_updates.parse_pins("pkg==1.0+local\n")
        assert "pkg" in pins

    def test_crlf_line_endings(self):
        """Windows-style line endings must parse identically to LF."""
        pins = check_updates.parse_pins("requests==2.28.0\r\nflask==3.0.0\r\n")
        assert pins == {"requests": "2.28.0", "flask": "3.0.0"}

    def test_duplicate_package_last_wins(self):
        """If a package is pinned twice, the later occurrence overrides.

        This is the dict-update semantics of the implementation; tested
        here so a future refactor (e.g. raise on duplicate) is deliberate.
        """
        pins = check_updates.parse_pins("requests==1.0\nrequests==2.0\n")
        assert pins == {"requests": "2.0"}

    def test_large_lockfile_performance(self):
        """Parsing 500 pins should complete well under one second."""
        lines = [f"package{i}=={i}.0.0" for i in range(500)]
        text = "\n".join(lines) + "\n"
        start = time.monotonic()
        pins = check_updates.parse_pins(text)
        elapsed = time.monotonic() - start
        assert len(pins) == 500
        assert elapsed < 0.5

    def test_pathological_input_does_not_hang(self):
        """A very long non-matching line must not trigger pathological backtracking."""
        text = "a" * 100_000 + "==1.0\n"  # eventually matches at the end
        start = time.monotonic()
        check_updates.parse_pins(text)
        elapsed = time.monotonic() - start
        assert elapsed < 1.0


