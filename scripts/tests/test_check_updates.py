class TestMain:
    def test_skips_missing_txt_file(self, tmp_path, monkeypatch):
        in_file = tmp_path / "requirements.in"
        in_file.write_text("requests\n")
        monkeypatch.setenv("ALL_FILES", json.dumps([str(in_file)]))
        monkeypatch.setenv("REPORTS_DIR", str(tmp_path))
        check_updates.REPORTS = tmp_path
        rc = check_updates.main()
        assert rc == 0
        updates = json.loads((tmp_path / "updates.json").read_text())
        assert updates == {}

    def test_returns_one_on_compile_errors(self, tmp_path, monkeypatch):
        in_file = tmp_path / "requirements.in"
        in_file.write_text("requests\n")
        (tmp_path / "requirements.txt").write_text("requests==2.28.0\n")
        monkeypatch.setenv("ALL_FILES", json.dumps([str(in_file)]))
        monkeypatch.setenv("REPORTS_DIR", str(tmp_path))
        check_updates.REPORTS = tmp_path

        with patch("subprocess.run") as mock_run:
            mock_run.return_value = MagicMock(
                returncode=1, stderr="pip-compile error", stdout=""
            )
            rc = check_updates.main()
        assert rc == 1

    def test_detects_upgrade_between_current_and_upgraded(self, tmp_path, monkeypatch):
        in_file = tmp_path / "requirements.in"
        in_file.write_text("requests\n")
        (tmp_path / "requirements.txt").write_text("requests==2.28.0\n")
        monkeypatch.setenv("ALL_FILES", json.dumps([str(in_file)]))
        monkeypatch.setenv("REPORTS_DIR", str(tmp_path))
        check_updates.REPORTS = tmp_path

        def fake_run(cmd, **_kwargs):
            out_flag = "--output-file"
            if out_flag in cmd:
                idx = cmd.index(out_flag)
                Path(cmd[idx + 1]).write_text("requests==2.31.0\n")
            return MagicMock(returncode=0, stderr="", stdout="")

        with patch("subprocess.run", side_effect=fake_run):
            rc = check_updates.main()

        assert rc == 0
        updates = json.loads((tmp_path / "updates.json").read_text())
        assert len(updates) > 0

    def test_empty_all_files_returns_zero(self, tmp_path, monkeypatch):
        monkeypatch.setenv("ALL_FILES", "[]")
        monkeypatch.setenv("REPORTS_DIR", str(tmp_path))
        check_updates.REPORTS = tmp_path
        assert check_updates.main() == 0

    def test_writes_updates_and_errors_json(self, tmp_path, monkeypatch):
        monkeypatch.setenv("ALL_FILES", "[]")
        monkeypatch.setenv("REPORTS_DIR", str(tmp_path))
        check_updates.REPORTS = tmp_path
        check_updates.main()
        assert (tmp_path / "updates.json").exists()
        assert (tmp_path / "pip_compile_errors.json").exists()
