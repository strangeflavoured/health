class TestDiffPins:
    def test_no_change_returns_empty(self):
        current = {"requests": "2.28.0", "flask": "3.0.0"}
        assert check_updates.diff_pins(current, current.copy()) == []

    def test_detects_upgrade(self):
        changes = check_updates.diff_pins(
            {"requests": "2.28.0"}, {"requests": "2.31.0"}
        )
        assert len(changes) == 1
        assert changes[0]["package"] == "requests"
        assert changes[0]["current"] == "2.28.0"
        assert changes[0]["latest"] == "2.31.0"

    def test_detects_new_package(self):
        changes = check_updates.diff_pins({}, {"requests": "2.31.0"})
        assert changes[0]["current"] == "(absent)"

    def test_detects_removed_package(self):
        changes = check_updates.diff_pins({"requests": "2.28.0"}, {})
        assert changes[0]["latest"] == "(removed)"

    def test_multiple_changes(self):
        current = {"a": "1.0", "b": "2.0", "c": "3.0"}
        upgraded = {"a": "1.1", "b": "2.0", "d": "4.0"}
        changes = check_updates.diff_pins(current, upgraded)
        packages = {c["package"] for c in changes}
        assert "a" in packages
        assert "c" in packages
        assert "d" in packages
        assert "b" not in packages

    def test_result_sorted_by_package_name(self):
        current = {"z": "1.0", "a": "1.0", "m": "1.0"}
        upgraded = {"z": "2.0", "a": "2.0", "m": "2.0"}
        changes = check_updates.diff_pins(current, upgraded)
        names = [c["package"] for c in changes]
        assert names == sorted(names)


